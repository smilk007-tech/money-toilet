/** 물내림 온보딩 사다리 — [오늘 물내림 최소 횟수, 보장 상위 %] 내림차순. 천장 70% */
export const LOOPANG_FLUSH_TIERS = [
  [5, 70],
  [4, 73],
  [3, 78],
  [2, 85], // 명세서 버튼이 2회차에 등장 → 사용자가 처음 보는 티어(+92→85 델타 동시 노출)
  [1, 92],
];

/** 오늘 체류초 + 오늘 물내림 횟수 → 기본 상위 % (오차 적용 전) */
export function tierPercentFor(todaySeconds, todayFlushes = 0) {
  const s = Math.max(0, Number(todaySeconds) || 0);
  const f = Math.max(0, Math.floor(Number(todayFlushes) || 0));
  let percent = LOOPANG_TIERS[0][1];
  for (const [minSec, p] of LOOPANG_TIERS) {
    if (s >= minSec) {
      percent = p;
      break;
    }
  }
  for (const [minFlush, p] of LOOPANG_FLUSH_TIERS) {
    if (f >= minFlush) {
      percent = Math.min(percent, p); // OR 보장 — 더 좋은(낮은) 쪽
      break;
    }
  }
  return percent;
}

/* ---------- 티어 ±1 오차 ----------
   전원이 같은 %를 들고 다니면 합성 곡선이 들통난다 → 기기·날짜·단계로 시드된
   결정적 오차(같은 사람·같은 날·같은 단계 = 항상 같은 값, 렌더마다 안 흔들림).
   상위 10% 이하(10/5/3/1)는 면제 — 꼭대기는 단계 간격이 2라 역전 위험이 있고,
   "상위 1%"는 깎으면 김새는 명예 숫자다. 사다리 최소 간격 3 > 오차폭 2×1이라 역전 불가. */
export const TIER_JITTER_EXEMPT_MAX = 10;

export function jitterTierPercent(basePercent, seed) {
  const base = Math.floor(Number(basePercent) || 0);
  if (base <= TIER_JITTER_EXEMPT_MAX) return base;
  let h = 2166136261;
  for (const c of String(seed)) {
    h ^= c.charCodeAt(0);
    h = Math.imul(h, 16777619) >>> 0;
  }
  return Math.min(99, Math.max(1, base + ((h % 3) - 1)));
}

/** 1억 월급 × 1시간 타이머 기준 1회 적립 상한 (초당 올림 × 3600) */
export function maxSessionEarnCap() {
  const maxSalary = Math.max(...SALARY_STEPS);
  const workSecPerMonth =
    WORK_CONFIG.workDaysPerMonth * WORK_CONFIG.workHoursPerDay * 3600;
  return Math.ceil(maxSalary / workSecPerMonth) * MAX_SESSION_SECONDS;
}

