/* ---------- 게임 기본 설정 ---------- */
export const WORK_CONFIG = {
  workDaysPerMonth: 22,
  workHoursPerDay: 8,
};

/* ---------- 월급 ---------- */
// 슬라이더 스텝 (원): 0(참는중) → 100~1000만(100만 단위) → 2000 → 5000 → 1억
export const SALARY_STEPS = [
  0, 100, 200, 300, 400, 500, 600, 700, 800, 900, 1000, 2000, 5000, 10000,
].map((m) => m * 10000);

export const DEFAULT_SALARY = 3_000_000;

/** 화장실 타이머 최대(초) — 강제 물내림 기준 */
export const MAX_SESSION_SECONDS = 3600;

/** 1억 월급 × 1시간 타이머 기준 1회 적립 상한 (초당 올림 × 3600) */
export function maxSessionEarnCap() {
  const maxSalary = Math.max(...SALARY_STEPS);
  const workSecPerMonth =
    WORK_CONFIG.workDaysPerMonth * WORK_CONFIG.workHoursPerDay * 3600;
  return Math.ceil(maxSalary / workSecPerMonth) * MAX_SESSION_SECONDS;
}

