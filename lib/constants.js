/* ---------- 루팡 티어 (오늘 체류시간 + 물내림 온보딩 → 상위 %) ----------
   실측 랭킹이 아니라 고정 합성 곡선. 이중 사다리:
   · 시간 사다리 — 진짜 지표. 하루(KST) 안에 완주 가능, 자정 리셋은 game.js 누산기가 담당.
   · 물내림 온보딩 — 최초 5분 체감용 최저 보장선(천장 70%). 연타해도 70%에서 정지,
     자랑할 만한 등급(그 밑)은 전부 시간으로만 도달 → 지표 오염 없음.
   최종 = 둘 중 더 좋은 쪽(min). 온보딩 종료(5회≈8분)와 시간 사다리 70%(8분)가 맞물려
   적극 유저는 첫 12분간 매 물내림마다 상승이 보인다. */

/** 시간 사다리 — [최소 오늘 체류초, 상위 %] 내림차순 */
